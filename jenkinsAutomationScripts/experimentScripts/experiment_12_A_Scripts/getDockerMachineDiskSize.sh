# !/bin/bash

DOCKER_MACHINE_FILEPATH="<REDACTED>/.docker/machine/machines/"
DOCKER_MACHINE_NAME="concurrentFitnesseTestingDockerMachine"
EXPERIMENT_12_A_STORAGE_FILEPATH="Experiment_12_A_Docker_Machine_Disk_Usage.txt"

date +"%Y-%m-%d %H:%M:%S" >> "${EXPERIMENT_12_A_STORAGE_FILEPATH}"

DOCKER_MACHINE_SIZE_KB=$(du -sk "${DOCKER_MACHINE_FILEPATH}${DOCKER_MACHINE_NAME}" | awk '{print $1}') 
DOCKER_MACHINE_SIZE_BYTES=$(( ${DOCKER_MACHINE_SIZE_KB} * 1000))
echo -e "${DOCKER_MACHINE_SIZE_BYTES}\n" >> "${EXPERIMENT_12_A_STORAGE_FILEPATH}"